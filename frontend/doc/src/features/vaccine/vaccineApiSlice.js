import { apiSlice } from "../../app/api/apiSlice"; // Importing apiSlice from apiSlice file

// Creating vaccineApiSlice using injectEndpoints method
export const vaccineApiSlice = apiSlice.injectEndpoints({
    endpoints: builder => ({
        // Query to get vaccine by ID
        getVaccineById: builder.query({
            query: (id) => ({
                url: `/api/vaccine/${id}`, // API endpoint URL
                method: "GET", // HTTP request method
            }),
        }),

        // Mutation to add patient vaccine by ID
        addPatientVaccineById: builder.mutation({
            query: (id, data) => ({
                url: `/api/patient/${id}/vaccine`, // API endpoint URL
                method: "POST", // HTTP request method
                body: { ...data }, // Request body
            }),
        }),

        // Query to get patient vaccine by ID
        getPatientVaccineById: builder.query({
            query: (id) => ({
                url: `/api/patient/${id}/vaccine`, // API endpoint URL
                method: "GET", // HTTP request method
            }),
        }),

        // Mutation to delete vaccine by ID
        deleteVaccineById: builder.mutation({
            query: (id) => ({
                url: `/api/vaccine/${id}`, // API endpoint URL
                method: "DELETE", // HTTP request method
            }),
        }),

        // Mutation to update vaccine by ID
        updateVaccineById: builder.mutation({
            query: (id, data) => ({
                url: `/api/vaccine/${id}`, // API endpoint URL
                method: "PUT", // HTTP request method
                data: { ...data } // Request body
            }),
        }),
    }),
});

// Exporting hooks generated by vaccineApiSlice
export const {
    useGetVaccineByIdQuery,
    useAddPatientVaccineByIdMutation,
    useGetPatientVaccineByIdQuery,
    useDeleteVaccineByIdMutation,
    useUpdateVaccineByIdMutation,
} = vaccineApiSlice;
